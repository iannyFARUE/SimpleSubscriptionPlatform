     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate(
            [
            'title'=>'required',
            'description'=>'required',
            'content'=>'required',
            'website_id' =>'required'
            ]
            );

            $post = $request->all();


            $post =  Post::create($post);

            $websiteSubscription = $post->website->users;
            Notification::send($websiteSubscription,new PostAdded($post));
            // return PostCollection::collection($post);

            return $post;
    }

